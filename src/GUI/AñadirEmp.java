    private void BTNAñadirActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_BTNAñadirActionPerformed
        String nombre = txtNombre.getText();
        String apellido = txtApellido.getText();
        String código = txtCódigo.getText();
        String contraseña = txtContraseña.getText();
        String confirmar = txtConfirmarContraseña.getText();
        String puesto = "";
        int N = CBPuesto.getSelectedIndex();
        if (N == 1){
            puesto = "Cajera";
        }
        if (N == 2){
            puesto = "Jefe";
        }
        if (contraseña.equals(confirmar)){
        if(contraseña == "" || confirmar == ""){
            BTNAñadir.setForeground(new java.awt.Color(255, 0, 0));
        }else{
            
            Empleados.agregarNuevoEmpleado(nombre,apellido, código,contraseña,confirmar,puesto);
        }
        }else{
            BTNAñadir.setForeground(new java.awt.Color(255, 0, 0));
        }
        
        
        
         
        
    }//GEN-LAST:event_BTNAñadirActionPerformed

